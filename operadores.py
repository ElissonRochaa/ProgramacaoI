# Adição -> +
# Subtração -> -
# Multiplicação -> *
# Divisão -> /
# Divisão inteira -> //
# Resto -> %
# Exponencial -> **

# Operador de atribuição
# =

numero1 = 15
numero2 = 6

adicao = numero1 + numero2
print(adicao)
subtracao = numero1 - numero2
print(subtracao)
mult = numero1 * numero2
print(mult)
div = numero1 / numero2
print(div)
div_inteira = numero1 // numero2
print(div_inteira)
resto = numero1 % numero2
print(resto)
expoente = numero1**numero2
print(expoente)